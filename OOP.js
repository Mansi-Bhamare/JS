/*OOPs 4 pillers
1.Abstraction:
-Abstraction is a process of hiding implementation details
 and exposes only the functionality to the user. In abstraction, we deal with ideas and not events. This means the user will only know “what it does” rather than “how it does”.
-Real-Life Example: 
 A driver will focus on the car functionality (Start/Stop -> Accelerate/ Break), he/she does not bother about how the Accelerate/ brake mechanism works internally.
 And this is how the abstraction works.

2.Encapsulation:
-Encapsulation is the process of wrapping code and data together into a single unit.
-Real-Life Example:
 A capsule which is mixed of several medicines. 
 The medicines are hidden data to the end user.

3.Inheritance: 
-Inheritance is the process of one class inheriting properties and methods from another class in Java. Inheritance is used when we have is-a relationship between objects.
-Real-life Example:
 The planet Earth and Mars inherits the super class Solar System and Solar system inherits the Milky Way Galaxy.
 So Milky Way Galaxy is the top super class for Class Solar System, Earth and Mars.

4.Polymorphism:
-polymorphism means many forms.
-Polymorphism is the ability to perform many things in many ways.
-(Real-life Example:
 A delivery person delivers items to the user. 
 If it’s a postman he will deliver the letters. 
 If it’s a food delivery boy he will deliver the foods to the user.
 Like this polymorphism implemented different ways for the delivery function.)*/